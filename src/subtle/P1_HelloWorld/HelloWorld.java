package subtle.P1_HelloWorld;

public class HelloWorld {

    public static void main(String[] args){
        // We need "static" for Java to find our method to run the code we are going to add below,
        // and "void" to determine we don't want for it to return anything

        // Hello World!
        System.out.println("Hello World!"); // prints out the traditional first line of text when learning a new lang

        // Variables examples
        int myFirstNumber = 15;
        int mySecondNumber = 5;
        int myThirdNumber = 10;
        int myTotal = myFirstNumber + mySecondNumber + myThirdNumber;
        System.out.println(myTotal); // prints out 30

    }

}













